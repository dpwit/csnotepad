A registered office address is a <strong>legal requirement</strong> for all Companies registered at Companies House. It is the official address of the incorporated company and is <strong>publicly available for anyone to view</strong>. Your registered office address can be different to that of your usual trading address. Your registered office will be used by Companies House, HMRC and other Government bodies to deliver statutory letters and legal notices. All companies are legally required to display the registered office address on <strong>business correspondence</strong> such as letters and the <strong>business website</strong>.
<br>
<br>
Your registered office address must be a <strong>real UK address, not a PO Box</strong>. 
<br>
<br>
Our registered office address service in Brighton is ideal as it provides you with a professional address in East Sussex, whilst keeping your private address details confidential, therefore reducing the risk of identity fraud.
