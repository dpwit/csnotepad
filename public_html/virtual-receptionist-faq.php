    </div>
	<br>
	<div class="faq">
      <div class="faq_question"><img src="/images/q.png" alt="Question" style="vertical-align: middle" />&nbsp;<u>Who answers my calls?</u></div>
           <div class="faq_answer_container">
              <div class="faq_answer"><img src="/images/a.png" alt="Answer" style="vertical-align: middle" />&nbsp;Each account is looked after by a dedicated team who are trained on your account, familiar with your business and who get to know your customers. Each team consists of 4 experienced receptionists who are first in line for your calls. If all 4 of your team happen to be busy on calls when one comes in, it will immediately overflow to a free receptionist on another team. Your caller will never be kept waiting.</div>
           </div>        
    </div>
