           <div class="faq_answer_container">
              <div class="faq_answer"><img src="/images/a.png" alt="Answer" style="vertical-align: middle" />&nbsp;Our core hours are Monday to Friday 8:30am - 6:00pm and Saturday 9:00am - 3:00pm, however you can have your calls answered 24/7 at no extra cost.</div>
