           <div class="faq_answer_container">
              <div class="faq_answer"><img src="/images/a.png" alt="Answer" style="vertical-align: middle" />&nbsp;Providing there are no unexpected delays we will have your account setup within 48 hours. If you require order taking or support ticket logging, we will have your account setup within 72 hours.</div>
           </div>        
    </div>
	<br>
	<div class="faq">
      <div class="faq_question"><img src="/images/q.png" alt="Question" style="vertical-align: middle" />&nbsp;<u>Why don’t you offer an instant set up?</u></div>
           <div class="faq_answer_container">
              <div class="faq_answer"><img src="/images/a.png" alt="Answer" style="vertical-align: middle" />&nbsp;For the same reason that you wouldn’t choose to hire a member of staff at 9am and have them on the phones at 9:02am, they wouldn’t know enough about who you are, what you do or how you do it. In short, we take your business seriously.</div>
           </div>        
    </div>
	<br>
	<div class="faq">
      <div class="faq_question"><img src="/images/q.png" alt="Question" style="vertical-align: middle" />&nbsp;<u>Do I have to have all my calls diverted to you?</u></div>
           <div class="faq_answer_container">
              <div class="faq_answer"><img src="/images/a.png" alt="Answer" style="vertical-align: middle" />&nbsp;No you don’t. You are able to have the service set up in many different ways. 1. All the calls can come directly to you on your landline or mobile and only divert to us if you are unavailable. 2. All the calls can come straight through to us all of the time or 3. You can switch between options 1 and 2 as and when you are in or out the office.</div>
