              <div class="faq_answer"><img src="/images/a.png" alt="Answer" style="vertical-align: middle" />&nbsp;We provide you with a unique telephone number when you register, which you divert your calls to. Our system recognises calls coming in on this number and your account information pops up on your receptionist's screen.</div>
           </div>        
    </div>
