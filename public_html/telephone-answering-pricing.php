			<br>
			<br>
			<strong style="color: #202020;">Why do companies use a telephone answering service?</strong>
			<br>
			<br>
			Some companies want to appear bigger, others want to sound more professional, but for most it’s because they know customers’ won’t leave a message, and so every time they miss a call they’re either irritating an existing customer or losing out on new business. 
			<br>
			<br>
			<strong style="color: #202020;">Can I trial the service?</strong>
			<br>
			<br>
			Yes. Your service comes with a 30 day trial period, so that you can make sure it's right for you. 
			<br>
			<br>
			<strong style="color: #202020;">Do you offer a free trial?</strong>
			<br>
			<br>
			No we don't. Over the years we've found that most people who are looking for a free trial are just looking for some free cover whilst they’re away on holiday.
			<br>
			<br>
			<strong style="color: #202020;">How long does it take to set the service up?</strong>
			<br>
			<br>
			1-3 days depending on what you’re after and how complicated it is. We can set you up the same day if needed, but we’d rather not.
			<br>
			<br>
			<strong style="color: #202020;">Do I set the service up online?</strong>
			<br>
			<br>
			No. We find that we get a much better feel for how you'd like your calls answered and how we should sound if you talk through the set up of your account with a scriptwriter.
			<br>
			<br>
			<strong style="color: #202020;">Do I have a specific person answering my calls?</strong>
			<br>
			<br>
			Yes... well actually you'll have a team of 4 specific people answering your calls, unless you’re really busy in which case you’ll have a team of 8, 12 or 16+ people.
			<br>
			<br>
			<strong style="color: #202020;">How do you know what to say on a call?</strong>
			<br>
			<br>
			Your scriptwriter will discuss this with you when setting your service up, and your team will then undergo specific training on your account. Also by having your own dedicated team of receptionists our knowledge of what you do and how you do it grows and grows.          
			<br>
			<br>
			<strong style="color: #202020;">Do you provide me with a number?</strong>
			<br>
			<br>
			Yes... it's usually hidden behind your existing number, but if you don't yet have a number for your business, or if you want another one, then we can provide you with pretty much any 01/02/08 or 07 number for free.
			<br>
			<br>
			<strong style="color: #202020;">Do I have to change my telephone number?</strong>
			<br>
			<br>
			No. Not unless you want to.
